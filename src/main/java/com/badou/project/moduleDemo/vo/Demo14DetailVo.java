package com.badou.project.moduleDemo.vo;

public class Demo14DetailVo {
	
	private int subMoney;
	private String customer;
	
	
	public Demo14DetailVo() {
		super();
	}
	public Demo14DetailVo(int subMoney, String customer) {
		super();
		this.subMoney = subMoney;
		this.customer = customer;
	}
	public int getSubMoney() {
		return subMoney;
	}
	public void setSubMoney(int subMoney) {
		this.subMoney = subMoney;
	}
	public String getCustomer() {
		return customer;
	}
	public void setCustomer(String customer) {
		this.customer = customer;
	}
	
	

}
